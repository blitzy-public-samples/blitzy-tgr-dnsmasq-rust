//! DHCPv4 protocol state machine implementation per RFC 2131.
//!
//! This module implements the complete DHCPv4 protocol state machine handling
//! the DISCOVER→OFFER→REQUEST→ACK message exchange sequence. It replaces the
//! C implementation in `src/rfc2131.c` with memory-safe Rust equivalents using
//! type-safe enums and structured error handling.
//!
//! # Protocol Flow
//!
//! ## Standard Four-Way Handshake
//!
//! 1. **Client → Server: DHCPDISCOVER**
//!    - Client broadcasts discover to find available DHCP servers
//!    - May include requested IP address, parameter list, hostname
//!    - Handled by [`DhcpProtocol::handle_discover`]
//!
//! 2. **Server → Client: DHCPOFFER**
//!    - Server offers an IP address with configuration parameters
//!    - Includes yiaddr (offered IP), lease time, server identifier
//!    - Generated by discover handler
//!
//! 3. **Client → Server: DHCPREQUEST**
//!    - Client requests the offered IP (or verifies existing lease)
//!    - Three contexts: selecting, init-reboot, renewing
//!    - Handled by [`DhcpProtocol::handle_request`]
//!
//! 4. **Server → Client: DHCPACK**
//!    - Server confirms allocation with full configuration
//!    - Lease becomes active, DNS registration occurs
//!    - Or DHCPNAK if request cannot be satisfied
//!
//! ## Additional Message Types
//!
//! - **DHCPDECLINE**: Client detected address conflict (ARP collision)
//!   - Handled by [`DhcpProtocol::handle_decline`]
//!   - Marks address as unavailable for configured backoff period
//!
//! - **DHCPRELEASE**: Client gracefully releases lease
//!   - Handled by [`DhcpProtocol::handle_release`]
//!   - Immediate lease termination, no NAK sent
//!
//! - **DHCPINFORM**: Client requests configuration without address
//!   - Handled by [`DhcpProtocol::handle_inform`]
//!   - Used when client has static IP but needs DNS/gateway info
//!
//! # RFC Compliance
//!
//! - RFC 2131: DHCP core protocol
//! - RFC 2132: DHCP options and BOOTP vendor extensions
//! - RFC 951: BOOTP compatibility mode
//! - RFC 4039: Rapid commit extension (two-message exchange)
//! - RFC 4388: DHCPv4 leasequery
//!
//! # Memory Safety
//!
//! Replaces C's manual buffer management with:
//! - Type-safe message parsing via [`DhcpMessage`]
//! - Enum-based option handling via [`DhcpOption`]
//! - Async lease operations via [`LeaseManager`]
//! - Bounds-checked slice operations throughout
//!
//! # Example
//!
//! ```rust,ignore
//! use std::sync::Arc;
//! use std::net::Ipv4Addr;
//!
//! // Create protocol handler
//! let protocol = DhcpProtocol::new(
//!     Arc::new(config),
//!     Arc::new(lease_manager),
//! );
//!
//! // Handle incoming DHCPDISCOVER
//! match protocol.handle_discover(&discover_msg).await {
//!     Ok(offer) => send_dhcp_reply(offer),
//!     Err(e) => warn!("Failed to generate offer: {}", e),
//! }
//! ```

