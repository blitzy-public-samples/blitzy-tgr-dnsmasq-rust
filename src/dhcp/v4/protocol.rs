    /// Selects appropriate DHCP context (address range) for a request.
    ///
    /// Matches request against configured DHCPv4 ranges based on:
    /// - Interface name (if request interface known)
    /// - Gateway relay address (giaddr)
    /// - Network address matching
    ///
    /// Replaces C function `complete_context()` and `narrow_context()`.
    ///
    /// # Arguments
    ///
    /// * `request` - DHCP request message to match
    ///
    /// # Returns
    ///
    /// Reference to matching DhcpRange, or None if no match found
    fn select_context<'a>(&'a self, request: &DhcpMessage) -> Option<&'a DhcpRange> {
        let giaddr = request.giaddr();

        // Find first matching DHCPv4 range
        // Priority: relay address match > interface match > first available
        for range in &self.config.v4_ranges {
            // Check if range matches relay address (giaddr)
            if !giaddr.is_unspecified() {
                if let std::net::IpAddr::V4(start_ipv4) = range.start {
                    if let std::net::IpAddr::V4(end_ipv4) = range.end {
                        // Simple check: if giaddr is within range network, use this range
                        // Full implementation would check network address with netmask
                        if self.ip_in_range(giaddr, start_ipv4, end_ipv4) {
                            return Some(range);
                        }
                    }
                }
            }

            // For now, return first IPv4 range as fallback
            // Full implementation would check interface matching
            if matches!(range.start, std::net::IpAddr::V4(_)) {
                return Some(range);
            }
        }

        None
    }

    /// Builds list of DHCP options for response message.
    ///
    /// Constructs option list including required options (message type, server ID,
    /// lease time) and requested options from client's parameter request list.
    ///
    /// Replaces C function `do_options()`.
    ///
    /// # Arguments
    ///
    /// * `context` - DHCP range context for this response
    /// * `request` - Client request message
    /// * `offered_ip` - IP address being offered/acknowledged
    /// * `lease_time` - Lease duration
    /// * `server_id` - Server identifier address
    /// * `message_type` - Response message type (Offer or Ack)
    ///
    /// # Returns
    ///
    /// Vector of DHCP options to include in response
    fn build_option_list(
        &self,
        context: &DhcpRange,
        request: &DhcpMessage,
        _offered_ip: Ipv4Addr,
        lease_time: Duration,
        server_id: Ipv4Addr,
        message_type: MessageType,
    ) -> Vec<DhcpOption> {
        let mut options = Vec::new();

        // Required options per RFC 2131
        options.push(DhcpOption::MessageType(message_type));
        options.push(DhcpOption::ServerId(server_id));

        // Lease time (not included for INFORM responses, handled by caller)
        if lease_time.as_secs() > 0 {
            options.push(DhcpOption::LeaseTime(lease_time.as_secs() as u32));
        }

        // Get parameter request list from client
        let requested_params = if let Some(DhcpOption::ParameterRequestList(params)) =
            request.get_option(|opt| matches!(opt, DhcpOption::ParameterRequestList(_)))
        {
            params.clone()
        } else {
            Vec::new()
        };

        // Add requested options (stub implementation - full version would read from config)
        for param_code in requested_params {
            match param_code {
                OPTION_NETMASK => {
                    // Calculate netmask from context (stub: use /24)
                    options.push(DhcpOption::Netmask(Ipv4Addr::new(255, 255, 255, 0)));
                }
                OPTION_ROUTER => {
                    // Use first IP in range as default gateway (stub)
                    if let std::net::IpAddr::V4(router) = context.start {
                        options.push(DhcpOption::Router(vec![router]));
                    }
                }
                OPTION_DNS_SERVER => {
                    // Stub: use common public DNS
                    options.push(DhcpOption::DnsServer(vec![
                        Ipv4Addr::new(8, 8, 8, 8),
                        Ipv4Addr::new(8, 8, 4, 4),
                    ]));
                }
                OPTION_DOMAIN_NAME => {
                    // Stub: use example domain
                    options.push(DhcpOption::DomainName("local".to_string()));
                }
                _ => {
                    // Unknown option code, skip
                    debug!(option_code = param_code, "Skipping unhandled option request");
                }
            }
        }

        // Check for rapid commit support (RFC 4039)
        if request.get_option(|opt| matches!(opt, DhcpOption::RapidCommit)).is_some() {
            options.push(DhcpOption::RapidCommit);
        }

        options
    }

    /// Calculates lease duration for a request.
    ///
    /// Determines lease time based on:
    /// - Per-range lease time override
    /// - Client requested lease time (option 51)
    /// - Global default lease time
    ///
    /// Replaces C function `calc_time()`.
    ///
    /// # Arguments
    ///
    /// * `context` - DHCP range context
    /// * `request` - Client request message
    ///
    /// # Returns
    ///
    /// Lease duration to use for this client
    fn calculate_lease_time(&self, context: &DhcpRange, request: &DhcpMessage) -> Duration {
        // Check for range-specific lease time
        if let Some(range_lease_time) = context.lease_time_override {
            return range_lease_time;
        }

        // Check for client requested lease time
        if let Some(DhcpOption::LeaseTime(requested_secs)) =
            request.get_option(|opt| matches!(opt, DhcpOption::LeaseTime(_)))
        {
            let requested_duration = Duration::from_secs(*requested_secs as u64);

            // Honor request if reasonable (not too short, not too long)
            let min_lease = Duration::from_secs(600); // 10 minutes minimum
            let max_lease = Duration::from_secs(86400 * 7); // 7 days maximum

            if requested_duration >= min_lease && requested_duration <= max_lease {
                return requested_duration;
            }
        }

        // Use global default
        self.config.lease_time
    }

    /// Determines server identifier (our IP address) for response.
    ///
    /// Selects appropriate server IP based on:
    /// - Relay address (giaddr) - use address on that network
    /// - Direct request - use address on client's network
    /// - Fallback - use first address in range
    ///
    /// Replaces C function `server_id()`.
    ///
    /// # Arguments
    ///
    /// * `context` - DHCP range context
    /// * `request` - Client request message
    ///
    /// # Returns
    ///
    /// IPv4 address to use as server identifier
    fn determine_server_id(&self, context: &DhcpRange, request: &DhcpMessage) -> Ipv4Addr {
        let giaddr = request.giaddr();

        // If request came through relay, use address on relay's network
        if !giaddr.is_unspecified() {
            // Stub: return giaddr itself (real implementation would find our IP on that network)
            return giaddr;
        }

        // Direct request: use address from context
        // Stub: use first address in range
        if let std::net::IpAddr::V4(server_ip) = context.start {
            return server_ip;
        }

        // Fallback: use broadcast address (should not happen)
        Ipv4Addr::new(255, 255, 255, 255)
    }

    /// Determines server identifier for REQUEST message processing.
    ///
    /// Similar to determine_server_id but specifically for REQUEST handling.
    fn determine_server_id_for_request(&self, _request: &DhcpMessage) -> Ipv4Addr {
        // Stub: return first available range's start address
        if let Some(range) = self.config.v4_ranges.first() {
            if let std::net::IpAddr::V4(server_ip) = range.start {
                return server_ip;
            }
        }

        Ipv4Addr::new(255, 255, 255, 255)
    }

    /// Finds static lease configuration for a MAC address.
    ///
    /// # Arguments
    ///
    /// * `mac` - Client hardware address
    ///
    /// # Returns
    ///
    /// Reference to static lease if configured, None otherwise
    fn find_static_lease(&self, mac: &MacAddress) -> Option<&StaticLease> {
        self.config.static_leases.iter().find(|lease| lease.mac == *mac)
    }

    /// Checks if an IP address is within a range.
    ///
    /// # Arguments
    ///
    /// * `ip` - IP address to check
    /// * `start` - Range start address
    /// * `end` - Range end address
    ///
    /// # Returns
    ///
    /// true if ip is within [start, end] inclusive
    fn ip_in_range(&self, ip: Ipv4Addr, start: Ipv4Addr, end: Ipv4Addr) -> bool {
        let ip_u32 = u32::from(ip);
        let start_u32 = u32::from(start);
        let end_u32 = u32::from(end);

        ip_u32 >= start_u32 && ip_u32 <= end_u32
    }

    /// Allocates an address from a DHCP range.
    ///
    /// Attempts to honor requested IP if present and available, otherwise
    /// finds first available address in range.
    ///
    /// # Arguments
    ///
    /// * `context` - DHCP range to allocate from
    /// * `request` - Client request message
    /// * `client_mac` - Client hardware address
    ///
    /// # Returns
    ///
    /// Ok(allocated IP address) or Err if no addresses available
    async fn allocate_address_from_range(
        &self,
        context: &DhcpRange,
        request: &DhcpMessage,
        client_mac: &MacAddress,
    ) -> Result<std::net::IpAddr, DhcpError> {
        // Check if client requested specific IP
        if let Some(DhcpOption::RequestedIpAddress(req_ip)) =
            request.get_option(|opt| matches!(opt, DhcpOption::RequestedIpAddress(_)))
        {
            // Verify requested IP is in range
            if let std::net::IpAddr::V4(start_ipv4) = context.start {
                if let std::net::IpAddr::V4(end_ipv4) = context.end {
                    if self.ip_in_range(*req_ip, start_ipv4, end_ipv4) {
                        // Check if IP is available
                        if self
                            .lease_manager
                            .find_by_ip(&std::net::IpAddr::V4(*req_ip))
                            .await
                            .is_none()
                        {
                            debug!(
                                client_mac = %client_mac,
                                requested_ip = %req_ip,
                                "Honoring requested IP address"
                            );
                            return Ok(std::net::IpAddr::V4(*req_ip));
                        }
                    }
                }
            }
        }

        // Find first available IP in range
        if let (std::net::IpAddr::V4(start_ipv4), std::net::IpAddr::V4(end_ipv4)) =
            (context.start, context.end)
        {
            let start_u32 = u32::from(start_ipv4);
            let end_u32 = u32::from(end_ipv4);

            for ip_u32 in start_u32..=end_u32 {
                let candidate_ip = Ipv4Addr::from(ip_u32);
                let candidate_addr = std::net::IpAddr::V4(candidate_ip);

                // Check if this IP is available
                if self.lease_manager.find_by_ip(&candidate_addr).await.is_none() {
                    debug!(
                        client_mac = %client_mac,
                        allocated_ip = %candidate_ip,
                        "Allocated new IP from range"
                    );
                    return Ok(candidate_addr);
                }
            }
        }

        // No addresses available
        Err(DhcpError::NoAddressAvailable {
            pool_name: format!("{}-{}", context.start, context.end),
        })
    }

    /// Handles DHCPREQUEST in SELECTING state.
    ///
    /// Client is accepting this server's offer.
    async fn handle_request_selecting(
        &self,
        request: &DhcpMessage,
        requested_ip: Ipv4Addr,
        server_id: Ipv4Addr,
    ) -> Result<DhcpMessage, DhcpError> {
        let client_mac = request.client_hardware_addr()?;

        // Verify requested IP is in our ranges
        let context = self.select_context(request).ok_or_else(|| {
            warn!(
                client_mac = %client_mac,
                requested_ip = %requested_ip,
                "No matching context for REQUEST"
            );
            DhcpError::NoAddressAvailable { pool_name: "no matching DHCP range".to_string() }
        })?;

        // Check if IP is available or already leased to this client
        if let Some(existing_lease) =
            self.lease_manager.find_by_ip(&std::net::IpAddr::V4(requested_ip)).await
        {
            // Verify it's leased to this client
            if existing_lease.mac != Some(client_mac) {
                warn!(
                    client_mac = %client_mac,
                    requested_ip = %requested_ip,
                    "IP already leased to different client"
                );
                return self.send_nak(request, "Address already allocated");
            }
        }

        // Allocate/renew the lease
        let lease_time = self.calculate_lease_time(context, request);
        let client_hostname =
            request.get_option(|opt| matches!(opt, DhcpOption::Hostname(_))).and_then(|opt| {
                if let DhcpOption::Hostname(hostname) = opt {
                    Some(hostname.clone())
                } else {
                    None
                }
            });

        match self
            .lease_manager
            .allocate_lease(
                std::net::IpAddr::V4(requested_ip),
                Some(client_mac),
                client_hostname,
                None,      // client_id not extracted yet
                "unknown", // interface not tracked yet
                lease_time,
            )
            .await
        {
            Ok(_lease) => {
                // Generate ACK
                let mut ack = DhcpMessage::new_reply(request);
                ack.set_yiaddr(requested_ip);

                let options = self.build_option_list(
                    context,
                    request,
                    requested_ip,
                    lease_time,
                    server_id,
                    MessageType::Ack,
                );

                for option in options {
                    ack.add_option(option);
                }

                info!(
                    client_mac = %client_mac,
                    acked_ip = %requested_ip,
                    lease_time_secs = lease_time.as_secs(),
                    "Generated DHCPACK (SELECTING)"
                );

                Ok(ack)
            }
            Err(e) => {
                error!(
                    client_mac = %client_mac,
                    requested_ip = %requested_ip,
                    error = %e,
                    "Failed to allocate lease"
                );
                self.send_nak(request, "Lease allocation failed")
            }
        }
    }

    /// Handles DHCPREQUEST in INIT-REBOOT state.
    ///
    /// Client is verifying cached address after reboot.
    async fn handle_request_init_reboot(
        &self,
        request: &DhcpMessage,
        requested_ip: Ipv4Addr,
    ) -> Result<DhcpMessage, DhcpError> {
        let client_mac = request.client_hardware_addr()?;

        // Verify requested IP is appropriate for client's network
        let context = self.select_context(request).ok_or_else(|| {
            warn!(
                client_mac = %client_mac,
                requested_ip = %requested_ip,
                "No matching context for INIT-REBOOT"
            );
            DhcpError::NoAddressAvailable { pool_name: "no matching DHCP range".to_string() }
        })?;

        // Check if IP is in range
        if let (std::net::IpAddr::V4(start), std::net::IpAddr::V4(end)) =
            (context.start, context.end)
        {
            if !self.ip_in_range(requested_ip, start, end) {
                warn!(
                    client_mac = %client_mac,
                    requested_ip = %requested_ip,
                    "Requested IP not in configured range"
                );
                return self.send_nak(request, "Wrong network");
            }
        }

        // Check if IP is available or leased to this client
        if let Some(existing_lease) =
            self.lease_manager.find_by_ip(&std::net::IpAddr::V4(requested_ip)).await
        {
            if existing_lease.mac != Some(client_mac) {
                warn!(
                    client_mac = %client_mac,
                    requested_ip = %requested_ip,
                    "IP leased to different client"
                );
                return self.send_nak(request, "Address in use");
            }
            // IP is leased to this client, renew it
        }

        // Allocate/renew lease and send ACK
        let lease_time = self.calculate_lease_time(context, request);
        let server_id = self.determine_server_id(context, request);

        match self
            .lease_manager
            .allocate_lease(
                std::net::IpAddr::V4(requested_ip),
                Some(client_mac),
                None,
                None,
                "unknown",
                lease_time,
            )
            .await
        {
            Ok(_) => {
                let mut ack = DhcpMessage::new_reply(request);
                ack.set_yiaddr(requested_ip);

                let options = self.build_option_list(
                    context,
                    request,
                    requested_ip,
                    lease_time,
                    server_id,
                    MessageType::Ack,
                );

                for option in options {
                    ack.add_option(option);
                }

                info!(
                    client_mac = %client_mac,
                    acked_ip = %requested_ip,
                    "Generated DHCPACK (INIT-REBOOT)"
                );

                Ok(ack)
            }
            Err(e) => {
                error!(
                    client_mac = %client_mac,
                    error = %e,
                    "Failed to allocate lease in INIT-REBOOT"
                );
                self.send_nak(request, "Lease allocation failed")
            }
        }
    }

    /// Handles DHCPREQUEST in RENEWING/REBINDING state.
    ///
    /// Client is extending existing lease.
    async fn handle_request_renewing(
        &self,
        request: &DhcpMessage,
        ciaddr: Ipv4Addr,
    ) -> Result<DhcpMessage, DhcpError> {
        let client_mac = request.client_hardware_addr()?;

        // Verify lease exists and belongs to this client
        let existing_lease =
            self.lease_manager.find_by_ip(&std::net::IpAddr::V4(ciaddr)).await.ok_or_else(
                || {
                    warn!(
                        client_mac = %client_mac,
                        ciaddr = %ciaddr,
                        "No lease found for RENEWING"
                    );
                    DhcpError::LeaseNotFound { ip: ciaddr.to_string() }
                },
            )?;

        if existing_lease.mac != Some(client_mac) {
            warn!(
                client_mac = %client_mac,
                ciaddr = %ciaddr,
                "Lease belongs to different client"
            );
            return self.send_nak(request, "Not your lease");
        }

        // Renew the lease
        let context = self.select_context(request).ok_or(DhcpError::NoAddressAvailable {
            pool_name: "no matching DHCP range".to_string(),
        })?;
        let lease_time = self.calculate_lease_time(context, request);
        let server_id = self.determine_server_id(context, request);

        match self.lease_manager.renew_lease(&std::net::IpAddr::V4(ciaddr), lease_time).await {
            Ok(_) => {
                let mut ack = DhcpMessage::new_reply(request);
                ack.set_yiaddr(ciaddr);

                let options = self.build_option_list(
                    context,
                    request,
                    ciaddr,
                    lease_time,
                    server_id,
                    MessageType::Ack,
                );

                for option in options {
                    ack.add_option(option);
                }

                info!(
                    client_mac = %client_mac,
                    renewed_ip = %ciaddr,
                    lease_time_secs = lease_time.as_secs(),
                    "Generated DHCPACK (RENEWING)"
                );

                Ok(ack)
            }
            Err(e) => {
                error!(
                    client_mac = %client_mac,
                    ciaddr = %ciaddr,
                    error = %e,
                    "Failed to renew lease"
                );
                self.send_nak(request, "Lease renewal failed")
            }
        }
    }

    /// Sends DHCPNAK response.
    ///
    /// # Arguments
    ///
    /// * `request` - Original request message
    /// * `message` - Human-readable error message
    ///
    /// # Returns
    ///
    /// DHCPNAK message
    fn send_nak(&self, request: &DhcpMessage, message: &str) -> Result<DhcpMessage, DhcpError> {
        let client_mac = request.client_hardware_addr()?;

        let mut nak = DhcpMessage::new_reply(request);
        nak.add_option(DhcpOption::MessageType(MessageType::Nak));
        nak.add_option(DhcpOption::Message(message.to_string()));

        warn!(
            client_mac = %client_mac,
            message = %message,
            "Sending DHCPNAK"
        );

        Ok(nak)
    }
